# Membuat set A and B  
A = {1, 2, 3, 4, 5}  
B = {4, 5, 6, 7, 8}  
 
print('A:',A) 
print('B:',B) 
# Gabungan menggunakan operator |  
print('A gabung B: ',(A | B)) 
 
# Irisan menggunakan operator &  
print('A irisan B: ',(A & B)) 
 
# operasi selisih 
print('A - B: ',(A - B)) 
 
# operasi komplemen 
print('A komplemen B: ',(A ^ B)) 